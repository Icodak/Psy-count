            <li>Droit à la limitation du traitement des Données à caractère personnel des Utilisateurs
                (article 18 RGPD) : </br> Ce droit signifie que le traitement des Données à caractère personnel
                des Utilisateurs auquel PSY-FI peut procéder est limité, de sorte que les Données à
                caractère personnel sont conservées, mais que PSY-FI ne peut pas les utiliser ni les
                traiter.</li>
            <li>Droit d’opposition au traitement des Données à caractère personnel des Utilisateurs
