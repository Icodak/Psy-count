                leurs Données à caractère personnel lorsque le traitement est basé sur l’intérêt légitime
                de PSY-FI.</li>
            <li>Droit à la portabilité des Données à caractère personnel que les Utilisateurs auront
                fournies, lorsque ces Données à caractère personnel font l’objet de traitements
                automatisés fondés sur leur consentement ou sur un contrat (article 20 RGPD). </br> Les
                Utilisateurs ont le droit de déplacer, copier ou transmettre les Données à caractère
                personnel les concernant de la base de donnée de PSY-FI vers une autre.</li>
            <li>Droit de définir le sort des Données à caractère personnel des Utilisateurs après leur
